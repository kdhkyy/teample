package com.jang.order;

public class OrderPicVO {

	private int picSeq;
	private String picOriginname;
	private String picSysname;
	private String picPpath;
	private int oSeq;
	private String orderYn;
	
	
	
	public int getpicSeq() {
		return picSeq;
	}
	public void setpicSeq(int picSeq) {
		this.picSeq = picSeq;
	}
	public String getpicOriginname() {
		return picOriginname;
	}
	public void setpicOriginname(String picOriginname) {
		this.picOriginname = picOriginname;
	}
	public String getpicSysname() {
		return picSysname;
	}
	public void setpicSysname(String picSysname) {
		this.picSysname = picSysname;
	}
	public String getpicPpath() {
		return picPpath;
	}
	public void setpicPpath(String picPpath) {
		this.picPpath = picPpath;
	}
	public int getoSeq() {
		return oSeq;
	}
	public void setoSeq(int oSeq) {
		this.oSeq = oSeq;
	}
	public String getorderYn() {
		return orderYn;
	}
	public void setorderYn(String orderYn) {
		this.orderYn = orderYn;
	}
	
	
}
